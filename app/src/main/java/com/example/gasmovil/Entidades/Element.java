package com.example.gasmovil.Entidades;

public class Element {
    private Integer id_e;
    private String nombre_e;
    private String codigo_e;

    public Integer getId_e() {
        return id_e;
    }

    public void setId_e(Integer id_e) {
        this.id_e = id_e;
    }

    public String getNombre_e() {
        return nombre_e;
    }

    public void setNombre_e(String nombre_e) {
        this.nombre_e = nombre_e;
    }

    public String getCodigo_e() {
        return codigo_e;
    }

    public void setCodigo_e(String codigo_e) {
        this.codigo_e = codigo_e;
    }
}
